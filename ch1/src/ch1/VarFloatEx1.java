package ch1;

public class VarFloatEx1 {
    public static void main(String[] args) {
        float score1 = 90.14f;

        double score2 = 98.5d;
        System.out.println("Score1 = " + score1);
        System.out.println("Score2 = " + score2);
        System.out.printf("Score2 = %.1f\n", score2);
    }
}
